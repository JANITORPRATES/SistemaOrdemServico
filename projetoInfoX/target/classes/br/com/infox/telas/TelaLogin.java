    public void logar() {
        String sql = "select * from tbusuarios where login=? and senha=?";
        try {
            /* as linhas abaixo preparam a consulta ao banco em função do que
            foi digitado nas caixas de texto. O ? é substituído pelo conteúdo */
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            String password = new String(txtSenha.getPassword());
            pst.setString(2, password);
            
            rs = pst.executeQuery();
            
            if (rs.next()) {
                // a linha abaixo obtem o conteúdo do campo perfil da tbusuarios
                String perfil = rs.getString(6);
                //System.out.println(perfil);
                
                TelaPrincipal principal = new TelaPrincipal();
                principal.setVisible(true);
                
                // a estrutura abaixo faz o tratamento do perfil do usuário
                if(perfil.equals("admin")){
                    TelaPrincipal.menuCadUsuario.setEnabled(true);
                    TelaPrincipal.menuRelServicos.setEnabled(true);
                    TelaPrincipal.lblUsuario.setForeground(Color.red);
                }
                
                TelaPrincipal.lblUsuario.setText(rs.getString(2));
                
                this.dispose();
                conexao.close();
            } else {
                JOptionPane.showMessageDialog(null, "Usuário ou senha inválido(s)");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

